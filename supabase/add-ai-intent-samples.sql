-- AI 메시지 추천 샘플 의도 관리 기능 추가
-- 사용자가 AI 추천 시 사용할 의도 샘플을 커스터마이징할 수 있도록 함

-- ============================================
-- 1. user_settings 테이블에 AI 의도 샘플 필드 추가
-- ============================================

ALTER TABLE user_settings
ADD COLUMN IF NOT EXISTS ai_intent_samples TEXT[] DEFAULT ARRAY[
  '기존 대화를 확인하고 온화한 말투로 고객 안부를 물어주는 문자',
  '고객과의 관계를 유지하며 간단한 인사 문자',
  '새로운 상품이나 서비스를 소개하는 영업 문자',
  '약속이나 미팅 일정을 확인하는 문자',
  '고객의 문의나 불만에 대한 답변 문자',
  '명절이나 기념일 인사 문자',
  '고객의 구매 결정을 돕는 추진 문자'
];

-- 주석 추가
COMMENT ON COLUMN user_settings.ai_intent_samples IS 'AI 메시지 추천 시 사용할 의도 샘플 목록 (사용자 커스터마이징 가능)';

